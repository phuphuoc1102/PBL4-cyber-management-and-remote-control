    private void buttonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonUpdateActionPerformed
        if (txtID.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "ID must be not null!");
            return;
        }
        if (txtName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Name must be not null!");
            return;
        }
        if (txtPrice.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Price must be not null!");
            return;
        }
        if (combobox.getSelectedItem().equals("Choose")) {
            JOptionPane.showMessageDialog(null, "Category must be not null!");
            return;
        }
        if (MenuRepository.getInstance().update(UUID.fromString(txtID.getText()),txtName.getText(), txtPrice.getText(), (String) combobox.getSelectedItem())) {
            JOptionPane.showMessageDialog(null, "Update success!!");
            txtID.setText("");
            txtName.setText("");
            txtPrice.setText("");
            combobox.setSelectedItem("Choose");
            getListMenuByCategoryName(MenuCategory.DRINK);
        } else {
            JOptionPane.showMessageDialog(null, "Error!!");
        }
    }//GEN-LAST:event_buttonUpdateActionPerformed

    private void buttonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonDeleteActionPerformed
        if (txtID.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "ID must be not null!");
            return;
        }
        if (txtName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Name must be not null!");
            return;
        }
        if (txtPrice.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Price must be not null!");
            return;
        }
        if (combobox.getSelectedItem().equals("Choose")) {
            JOptionPane.showMessageDialog(null, "Category must be not null!");
            return;
        }
        if (MenuRepository.getInstance().delete(UUID.fromString(txtID.getText()))) {
            JOptionPane.showMessageDialog(null, "Delete success!!");
            txtID.setText("");
            txtName.setText("");
            txtPrice.setText("");
            combobox.setSelectedItem("Choose");
            getListMenuByCategoryName(MenuCategory.DRINK);
        } else {
            JOptionPane.showMessageDialog(null, "Error!!");
        }
    }//GEN-LAST:event_buttonDeleteActionPerformed

