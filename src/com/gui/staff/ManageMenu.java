    private void tblDisplayMouseClicked(MouseEvent evt) {
        int row = jTableMenu.getSelectedRow();
        List<Menu> list = MenuRepository.getInstance().getListMenuByCategory(MenuCategory.DRINK);
        idSave = list.get(row).getId();
        txtID.setText(idSave.toString());
        txtName.setText(jTableMenu.getValueAt(row, 1) + "");
        combobox.setSelectedItem(jTableMenu.getValueAt(row, 2) + "");
        txtPrice.setText(jTableMenu.getValueAt(row, 3) + "");
    }
