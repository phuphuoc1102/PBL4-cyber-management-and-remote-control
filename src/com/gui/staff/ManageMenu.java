    private void getListMenuByCategoryName(MenuCategory category) {
        tableModel.setRowCount(0);
        List<Menu> list = MenuRepository.getInstance().getListMenuByCategory(category);
        for (int i = 0; i < list.size(); i++) {
            Menu menu = list.get(i);
            Object[] dt = {menu.getId(), menu.getName(),menu.getCategory(),menu.getPrice()};
            tableModel.addRow(dt);
        }
    }
    private void findMenuByUsername(String key) {
        tableModel.setRowCount(0);
        List<Menu> list = MenuRepository.getInstance().getMenuByName(key);
        for (int i = 0; i < list.size(); i++) {
            Menu menu = list.get(i);
            Object[] dt = {menu.getId(), menu.getName(),menu.getCategory(),menu.getPrice()};
            tableModel.addRow(dt);
        }
    }
