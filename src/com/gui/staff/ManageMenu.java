    private void buttonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddActionPerformed
        if (!txtID.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Menu already exits!");
            return;
        }
        if (txtName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Name must be not null!");
            return;
        }
        if (txtPrice.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Price must be not null!");
            return;
        }
        if (combobox.getSelectedItem().equals("Choose")) {
            JOptionPane.showMessageDialog(null, "Category must be not null!");
            return;
        }
        if (MenuRepository.getInstance().create(txtName.getText(), txtPrice.getText(), (String) combobox.getSelectedItem())) {
            JOptionPane.showMessageDialog(null, "Add new success!!");
            txtID.setText("");
            txtName.setText("");
            txtPrice.setText("");
            combobox.setSelectedItem("Choose");
            getListMenuByCategoryName(MenuCategory.DRINK);
        } else {
            JOptionPane.showMessageDialog(null, "Error!!");
        }
    }//GEN-LAST:event_buttonAddActionPerformed

